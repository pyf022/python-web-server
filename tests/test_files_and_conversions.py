def test_text_extract_from_markdown(client, headers):
    upload = client.post(
        "/v1/files",
        headers=headers,
        files={"file": ("note.md", BytesIO(b"hello agent"), "text/markdown")},
    )
    file_id = upload.json()["file_id"]
    response = client.post("/v1/tools/text_extract/run", headers=headers, json={"input": {"file_id": file_id}})
    assert response.status_code == 200
    assert response.json()["result"]["data"]["text"] == "hello agent"
